grade = 65

if grade > 100:
    print("No way you got this. You cheated.")
elif grade == 100:
    print("Super! You got a 100!")
elif grade >= 90:
    print("You got an A! Congrats!")
elif grade >= 80:
    print("You got a B! Great work!")
elif grade >= 70:
    print("You got a C. Doing alright!")
elif grade >= 65:
    print("You got a D. Study harder!")
else:
    print("You got an F. You suck.")